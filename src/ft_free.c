#include "cub3D.h"

void    ft_free_strarr(char **str)
{
    size_t i;

    i = 0;
    while (str[i])
        i++;
    i--;
    while (i > 0)
    {
        free(str[i]);
        i--;
    }
    free(str[i]);
    free(str);
}